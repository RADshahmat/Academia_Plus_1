CREATE TABLE students (
    id VARCHAR2(6) PRIMARY KEY,
    student_name1 VARCHAR2(255),
    student_name2 VARCHAR2(255),
    phone_no VARCHAR2(255),
    father_name VARCHAR2(255),
    mother_name VARCHAR2(255),
    present_address VARCHAR2(255),
    permanent_address VARCHAR2(255),
    dob VARCHAR2(255),
    class VARCHAR2(255),
    image VARCHAR2(255),
    email VARCHAR2(255),
    city VARCHAR2(255),
    state VARCHAR2(255),
    zipcode VARCHAR2(255)
);
